public class ArithmeticsOperations {

    public static void main(String[] args) {

        int a = 4;
        int b = 5;
        int sum,dif,prod;
        int d = 0;

        sum = a+b;
        dif = a-b;
        prod = a*b;

        System.out.println("Sum -> "+sum + ", Dif -> " + dif + ", Prod -> " + prod + ".");

        d = d + 1;
        d += 1;
        d++;
        ++d;

    }
}
